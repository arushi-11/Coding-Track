
import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
	    Scanner sc = new Scanner(System.in);
	    int n = sc.nextInt();
	    for (int i=0; i<n; i++){
	        int a = sc.nextInt();
	        int b = sc.nextInt();
	        int z =0;
	        z= a%b;
	        System.out.println(z);
	    }
	}
}
